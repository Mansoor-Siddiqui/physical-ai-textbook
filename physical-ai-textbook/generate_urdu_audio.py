# Use Pakistani Urdu male voice
VOICE = "ur-PK-AsadNeural"
OUTPUT_DIR = "static/audio/ur"

async def generate_audio(episode_id: str, text: str, output_path: str):
    """Generate audio file from text using Edge TTS."""
    print(f"Generating audio for {episode_id}...")
    communicate = edge_tts.Communicate(text, VOICE)
    await communicate.save(output_path)
    print(f"  Saved: {output_path}")

async def main():
    """Generate all Urdu podcast audio files."""
    # Create output directory if it doesn't exist
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # Generate audio for each episode
    for episode_id, data in EPISODES.items():
        output_path = os.path.join(OUTPUT_DIR, f"{episode_id}.mp3")
        await generate_audio(episode_id, data["transcript"], output_path)

    print("\nAll Urdu audio files generated successfully!")
    print(f"Files saved in: {OUTPUT_DIR}/")

if __name__ == "__main__":
    asyncio.run(main())
